package Chapter1;

@SuppressWarnings({"unused"}) // 忽略不使用的警告
public class DataType{
    public static void main(String[] args){

        // 整数类型
        int num1 = 1;    // 32位
        byte num2 = 30;  // 8位 
        short num3 = 31; // 16位
        long num4 = 10L; // 64位

        // 浮点数
        float num5 = 10.1233f;
        double num6 = 10.123;

        // 单字符
        char name = 'a';

        // bool类型
        boolean flag = true;

        // 字符串类(是一个类，不是基本数据类型)
        // String name2 = "你好世界";

        System.out.print("你好");
    }
}